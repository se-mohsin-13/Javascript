let a = 10;
let b = 4;

console.log(`a + b : ${a + b}`);    // --> 14 
console.log(`a - b : ${a - b}`);    // --> 6
console.log(`a * b : ${a * b}`);    // --> 40
console.log(`a / b : ${a / b}`);    // --> 2.5
console.log(`a ** b : ${a ** b}`);  // --> 10000
console.log(`a % b : ${a % b}`);    // --> 2
console.log(`++a : ${++a}`);        // --> 11
console.log(`a++ : ${a++}`);        // --> 10
console.log(`--a : ${--a}`);        // --> 9
console.log(`a-- : ${a--}`);        // --> 10
