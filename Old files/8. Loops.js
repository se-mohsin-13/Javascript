"use strict";

// WHILE LOOP
console.log("While Loop:");
let i = 1;
while (i <= 5) {
  console.log("i =", i);
  i++;
}
console.log("----------------------");

// FOR LOOP
console.log("For Loop:");
for (let j = 1; j <= 5; j++) {
  console.log("j =", j);
}
console.log("----------------------");

// DO-WHILE LOOP
console.log("Do-While Loop:");
let k = 1;
do {
  console.log("k =", k);
  k++;
} while (k <= 5);
console.log("----------------------");
